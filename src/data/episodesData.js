export const getAllSeries = () => seriesData;

export const getSeriesById = (seriesId) => {
  return seriesData.find((series) => series.id === seriesId) || null;
};

export const getAllSeasons = (seriesId) => {
  const series = getSeriesById(seriesId);

  if (!series) {
    return [];
  }

  return Object.entries(series)
    .filter(([key]) => key.startsWith("season"))
    .map(([, seasonData]) => seasonData)
    .sort((a, b) => a.seasonNumber - b.seasonNumber);
};

export const getSeasonById = (seriesId, seasonNumber) => {
  return (
    getAllSeasons(seriesId).find(
      (season) => season.seasonNumber === Number(seasonNumber),
    ) || null
  );
};
